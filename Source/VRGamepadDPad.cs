namespace FlaxVR
{
    /// <summary>
    /// Represents the state of the directional pad.
    /// </summary>
    public struct VRGamepadDPad
    {
        /// <summary>
        /// The D-Pad up button.
        /// </summary>
        public VRButtonState Up;

        /// <summary>
        /// The D-Pad down button.
        /// </summary>
        public VRButtonState Down;

        /// <summary>
        /// The D-Pad left button.
        /// </summary>
        public VRButtonState Left;

        /// <summary>
        /// The D-Pad right button.
        /// </summary>
        public VRButtonState Right;
    }
}